export function createSeoMetadata(page: SeoPage): Metadata {
  const url = `${siteUrl}/${page.slug}`;

  return {
    title: page.metaTitle,
    description: page.description,
    alternates: {
      canonical: url,
    },
    openGraph: {
      title: page.metaTitle,
      description: page.description,
      url,
      siteName: "Virtual AI Photo Studio",
      images: [
        {
          url: "/before-after/after-luxury-garage-01.webp",
          width: 1024,
          height: 1280,
          alt: page.h1,
        },
      ],
      locale: "ru_RU",
      type: "website",
    },
    twitter: {
      card: "summary_large_image",
      title: page.metaTitle,
      description: page.description,
      images: ["/before-after/after-luxury-garage-01.webp"],
    },
  };
}

export function createFaqJsonLd(page: SeoPage) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: page.faq.map((item) => ({
      "@type": "Question",
      name: item.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.answer,
      },
    })),
  };
}

export function createServiceJsonLd(page: SeoPage) {
  return {
    "@context": "https://schema.org",
    "@type": "Service",
    name: page.title,
    description: page.description,
    provider: {
      "@type": "Organization",
      name: "Virtual AI Photo Studio",
      url: siteUrl,
    },
    areaServed: "RU",
    serviceType: page.primaryQuery,
    url: `${siteUrl}/${page.slug}`,
  };
}
